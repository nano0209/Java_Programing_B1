package day18_tasks;
/*
/ start from number 20 and go back to 1
 */

public class Backwards {
    public static void main(String[] args) {

        for (int i = 0; i <=20 ; i++) {
            System.out.println(i);

        }
        System.out.println("=================");
        for (int i = 20; i >=0 ; i--) {
            System.out.println(i);

        }

    }
}
